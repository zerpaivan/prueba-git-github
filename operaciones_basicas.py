# script para realizar operaciones matematicas basicas
def sumar(a, b):
    return a + b


def restar(a, b):
    return a - b


def multiplicar(a, b):
    return a * b


if __name__ == "__main__":

    a = 5
    b = 4
    print("suma: ", sumar(a, b))
    print("resta: ", restar(a, b))
    print("multiplicacion: ", multiplicar(a, b))
